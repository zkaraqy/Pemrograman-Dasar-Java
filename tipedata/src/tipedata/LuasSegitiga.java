package tipedata;

public class LuasSegitiga {

    public static void main(String args[]) {
        int alas = 5,
                tinggi = 8;
        double luasSegitiga = 0.5 * alas * tinggi;
        
        System.out.println("Luas Segitiga = 1/2 x alas x tinggi");
        System.out.println("Luas Segitiga = 1/2 x " + alas + " x " + tinggi + " = " + luasSegitiga);
    }
}
