public class VariableInteger01 {

    public static void main(String args[]) {
        // int -> bilangan bulat 1,2,100
        // byte, short, int, long
        int bilangan;
        bilangan = 13;
        System.out.println(bilangan);
        
        // double -> bilangan pecahan 1.2 6.99
        // double, float
        double desimal;
        desimal = 10.5;
        System.out.println(desimal);
    }
}
