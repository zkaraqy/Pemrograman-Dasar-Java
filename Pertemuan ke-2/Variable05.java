public class Variable05 {
    public static void main(String[] args) {

        // deklarasi variable
        String NIM = "2311016210003";
        String nama = "Aradea Ramadhan";

        // tulis ke layar
        System.out.println("NIM : " + NIM);
        System.out.println("Nama : " + nama);

        // space
        System.out.println("");

        // ubah nilai variable
        NIM = "2311016110005";
        nama = "Muhammad Azka Raki";

        // tulis ke layar
        System.out.println("NIM : " + NIM);
        System.out.println("Nama : " + nama);

    }
}